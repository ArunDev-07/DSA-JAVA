/*
===============================================================================
                     KTH SMALLEST ELEMENT IN A BST
===============================================================================

Platform  : LeetCode (230)
Approach  : Inorder Traversal
Language  : Java

===============================================================================
PROBLEM SUMMARY
===============================================================================

Given:

1) Root of a BST
2) Integer k

Return the kth smallest value in the BST.

===============================================================================
BST PROPERTY
===============================================================================

Inorder Traversal of BST gives:

Sorted Order

Example:

                5
              /   \
             3     7
            / \   / \
           2   4 6   8

Inorder:

2 3 4 5 6 7 8

===============================================================================
CORE IDEA
===============================================================================

Perform Inorder Traversal.

Each visited node represents the next smallest value.

Decrease k every time we visit a node.

When:

k == 0

Current node is the answer.

===============================================================================
FULL DRY RUN
===============================================================================

Tree:

                5
              /   \
             3     7
            / \   / \
           2   4 6   8

k = 3

-------------------------------------------------------------------------------

Inorder Order:

2 3 4 5 6 7 8

-------------------------------------------------------------------------------

Visit 2

k = 3 -> 2

-------------------------------------------------------------------------------

Visit 3

k = 2 -> 1

-------------------------------------------------------------------------------

Visit 4

k = 1 -> 0

Answer:

4

Return

===============================================================================
VISUALIZATION
===============================================================================

Sorted Sequence:

[2, 3, 4, 5, 6, 7, 8]

 k = 3

     ↓

Answer = 4

===============================================================================
RECURSION FLOW
===============================================================================

inOrder(5)
│
├── inOrder(3)
│   │
│   ├── inOrder(2)
│   │
│   └── Visit 3
│
└── Visit 4

k becomes 0

Answer Found

===============================================================================
TIME COMPLEXITY
===============================================================================

O(n)

Worst Case

===============================================================================
SPACE COMPLEXITY
===============================================================================

O(h)

h = Height of Tree

Recursion Stack

===============================================================================
PATTERN LEARNED
===============================================================================

BST + Inorder Traversal

Used In:

- Kth Smallest Element
- BST Iterator
- Validate BST
- Recover BST

===============================================================================
*/

class Solution {

    int ans = 0;

    int k;

    public int kthSmallest(TreeNode root, int k) {

        this.k = k;

        inOrder(root);

        return ans;
    }

    public void inOrder(TreeNode root) {

        if (root == null || k == 0) {
            return;
        }

        inOrder(root.left);

        k--;

        if (k == 0) {

            ans = root.val;

            return;
        }

        inOrder(root.right);
    }
}
